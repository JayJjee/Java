public class ex04 {
    public static void main(String[] args) {

        int pos = 5;
        
        switch(pos){
            case 1:
                System.out.println("Primeiro lugar");
                break;

            case 2:
                System.out.println("Segundo lugar");
                break;

            case 3:
                System.out.println("Terceiro lugar");
                break;

            default:
                System.out.println("Nao subiu ao podio");
                break;
        }
    }
}
